# -*- coding: utf-8 -*-
"""Typing Protocols para os mixins de ClientEditorDialog e ClientFilesDialog.

Criado na Fase 7 para eliminar ~379 warnings do Pyright/Pylance
(reportAttributeAccessIssue) nos mixins, sem alterar comportamento de runtime.

Uso: importado apenas sob TYPE_CHECKING nos mixins para anotar ``self``.
"""

from __future__ import annotations

import queue
import tkinter as tk
from concurrent.futures import ThreadPoolExecutor
from tkinter import ttk
from typing import Any, Callable, Protocol


# ============================================================================
# EditorDialogProto — descreve o "self" esperado pelos editor mixins
# ============================================================================


